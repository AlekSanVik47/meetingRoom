CREATE TABLE IF NOT EXISTS meeting_room.users
(
id smallint GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 32767 CACHE 1 )NOT NULL,
login VARCHAR(255)NOT NULL,
password VARCHAR(255)NOT NULL,
name VARCHAR(255)NOT NULL,
surname VARCHAR(255)NOT NULL,
patronymic VARCHAR(255),
phone character varying(20),
position VARCHAR(255),
recording_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
CONSTRAINT pk_user PRIMARY KEY(id),
CONSTRAINT user_phone_key UNIQUE(phone)
);


CREATE TABLE IF NOT EXISTS meeting_room.room(
id smallint GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 32767 CACHE 1 ) NOT NULL,
 room_number INTEGER NOT NULL,
 capacity INTEGER NOT NULL,
 television VARCHAR(255),
 recording_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
CONSTRAINT pk_room PRIMARY KEY(id)
);

CREATE TABLE IF NOT EXISTS meeting_room.meeting
(
id smallint GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 32767 CACHE 1 ) NOT NULL,
recording_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
CONSTRAINT pk_meeting PRIMARY KEY(id)
);